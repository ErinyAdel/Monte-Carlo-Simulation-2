    public void AddRows1()
    {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] rowData = new Object[2];
        
        c.calcCumulativeProb(Probability.vars);
        c.assignInterval(Probability.vars);
       
        for(int i=0; i<numOfData; i++) {
            rowData[0] = Probability.vars.get(i).getCumulativeProbability();
            rowData[1] = Probability.vars.get(i).getIntervalMin();
            
            model.addRow(rowData);
        }
    }
    
    public void AddRows2()
    {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        Object[] rowData = new Object[3];
            
        simulatedVariables = c.getSimulatedModelVariables(Probability.vars,numOfSimulations);
        
        int j = 1;
        for(int i=0; i<numOfSimulations; i++) 
        {
            rowData[0] = j;
            rowData[1] = simulatedVariables.get(i).getRandomNum();
            rowData[2] = simulatedVariables.get(i).getValue();
            j++;
            model.addRow(rowData);
        }
    }
    
