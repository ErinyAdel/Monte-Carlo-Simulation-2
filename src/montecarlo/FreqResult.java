    public void AddRows1()
    {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] rowData = new Object[3];
        
        c.calcProbabilityFromFreq(Frequency.vars);
        c.calcCumulativeProb(Frequency.vars);
        c.assignInterval(Frequency.vars);

        //jTable1.setDefaultRenderer(Double.class, (TableCellRenderer) this);
        
        for(int i=0; i<numOfData; i++) {
            rowData[0] = (float)Frequency.vars.get(i).getProbability();
            rowData[1] = Frequency.vars.get(i).getCumulativeProbability();
            rowData[2] = Frequency.vars.get(i).getIntervalMin();

            model.addRow(rowData);
        }
    }
    
    public void AddRows2()
    {
        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        Object[] rowData = new Object[3];
            
        simulatedVariables = c.getSimulatedModelVariables(Frequency.vars,numOfSimulations);
        
        int j = 0;
        for(int i=0; i<simulatedVariables.size(); i++) 
        {
            rowData[0] = ++j;
            rowData[1] = simulatedVariables.get(i).getRandomNum();
            rowData[2] = simulatedVariables.get(i).getValue();
            
            model.addRow(rowData);
        }
    }
    
