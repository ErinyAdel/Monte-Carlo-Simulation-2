    public void addFieldsData(int size)
    {
        for(int i=0; i<size; i++){
            listOfDataTexts.add(new JTextField());
        }
        int j = 4;
        for (int i = 0; i < size; i++) {
            listOfDataTexts.get(i).setBounds(100, 35 * j, 50, 25);
            listOfDataTexts.get(i).setVisible(true);
            add(listOfDataTexts.get(i));
            j++;
        }
    }

    public void addFieldsProb(int size)
    {
        for(int i=0; i<size; i++){
            listOfProbTexts.add(new JTextField());
        }
        int j = 4;
        for (int i = 0; i < size; i++) {
            listOfProbTexts.get(i).setBounds(490,35*j,50,25);
            add(listOfProbTexts.get(i));
            j++;
        }
    }

